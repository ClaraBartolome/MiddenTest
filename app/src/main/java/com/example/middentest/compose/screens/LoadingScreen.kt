@Composable
fun LoadingScreen(paddingValues: PaddingValues, size: Dp = 50.dp) {
    Box(modifier = Modifier.fillMaxWidth().height(IntrinsicSize.Min).padding(paddingValues), contentAlignment = Alignment.TopCenter) {
        AnimatedLoading(modifier = Modifier.size(size))
    }
}

@Composable
private fun AnimatedLoading(modifier: Modifier = Modifier) {
    val preloaderLottieComposition by rememberLottieComposition(
        LottieCompositionSpec.RawRes(
            R.raw.lottie_loading_anim
        )
    )

    val preloaderProgress by animateLottieCompositionAsState(
        preloaderLottieComposition,
        iterations = LottieConstants.IterateForever,
        isPlaying = true
    )


    LottieAnimation(
        composition = preloaderLottieComposition,
        progress = preloaderProgress,
        contentScale = ContentScale.Crop,
        modifier = modifier
    )
}

